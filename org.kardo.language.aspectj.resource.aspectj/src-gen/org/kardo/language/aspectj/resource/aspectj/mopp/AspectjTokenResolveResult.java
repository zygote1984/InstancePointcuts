package org.kardo.language.aspectj.resource.aspectj.mopp;

/**
 * A basic implementation of the ITokenResolveResult interface.
 */
public class AspectjTokenResolveResult implements org.kardo.language.aspectj.resource.aspectj.IAspectjTokenResolveResult {
	
	private String errorMessage;
	private Object resolvedToken;
	
	public AspectjTokenResolveResult() {
		super();
		clear();
	}
	
	public String getErrorMessage() {
		return errorMessage;
	}
	
	public Object getResolvedToken() {
		return resolvedToken;
	}
	
	public void setErrorMessage(String message) {
		errorMessage = message;
	}
	
	public void setResolvedToken(Object resolvedToken) {
		this.resolvedToken = resolvedToken;
	}
	
	public void clear() {
		errorMessage = "Can't resolve token.";
		resolvedToken = null;
	}
	
}
